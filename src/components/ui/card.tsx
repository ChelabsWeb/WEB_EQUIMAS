"use client";

import * as React from "react"
import { motion, HTMLMotionProps } from "framer-motion"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { 
    animated?: boolean;
    interactive?: boolean;
  }
>(({ className, animated = false, interactive = false, ...props }, ref) => {
  const Comp = animated ? motion.div : "div"
  const animationProps = animated ? {
    initial: { opacity: 0, y: 15 },
    whileInView: { opacity: 1, y: 0 },
    viewport: { once: true, margin: "-50px" },
    transition: { duration: 0.6, ease: [0.22, 1, 0.36, 1] }
  } : {}

  return (
    // @ts-ignore - Dynamic component typings with Framer Motion
    <Comp
      ref={ref}
      className={cn(
        "rounded-md border border-border/50 bg-card text-card-foreground transition-all duration-500 ease-out dark:border-white/10",
        interactive ? "cursor-pointer glass-premium hover:-translate-y-1 hover:shadow-xl active:scale-[0.98]" : "glass-premium",
        className
      )}
      {...animationProps}
      {...props}
    />
  )
})
Card.displayName = "Card"

